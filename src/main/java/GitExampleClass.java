public class GitExampleClass {

    public int sum(int firstNumb, int secondNumb, int numb) {
        return firstNumb + secondNumb + numb;
    }

    public static void main(String[] args) {
        GitExampleClass gitExampleClass = new GitExampleClass();
        System.out.println(gitExampleClass.sum(5, 6, 9));
    }
}
