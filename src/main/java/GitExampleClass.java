public class GitExampleClass {

    public int sum(int firstNumb, int secondNumb, int thirdNumb) {
        return firstNumb + secondNumb + thirdNumb;
    }

    public static void main(String[] args) {
        GitExampleClass gitExampleClass = new GitExampleClass();
        System.out.println(gitExampleClass.sum(5, 6, 7));
    }
}
